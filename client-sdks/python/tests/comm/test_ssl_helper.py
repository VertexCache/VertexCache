def test_create_verified_socket_context_valid_cert_should_succeed():
    ctx = SSLHelper.create_verified_socket_context(VALID_PEM_CERT)
    assert isinstance(ctx, ssl.SSLContext)


def test_create_verified_socket_context_invalid_cert_should_throw():
    with pytest.raises(VertexCacheSdkException):
        SSLHelper.create_verified_socket_context("not a cert")


def test_create_insecure_socket_context_should_succeed():
    ctx = SSLHelper.create_insecure_socket_context()
    assert isinstance(ctx, ssl.SSLContext)


def test_insecure_tls_connection_to_localhost_if_enabled():
    enable_live = True
    if not enable_live:
        pytest.skip("Live TLS test skipped; enable_live = False")

    ctx = SSLHelper.create_insecure_socket_context()
    try:
        with socket.create_connection(("localhost", 50505), timeout=1) as sock:
            with ctx.wrap_socket(sock, server_hostname="localhost") as ssock:
                assert ssock.cipher() is not None
    except Exception as e:
        pytest.skip(f"TLS connection failed: {e}")
